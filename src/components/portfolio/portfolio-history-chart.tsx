export const PortfolioHistoryChart = () => {
  const [period, setPeriod] = useState('twenty_four_hours');

  const option = useMemo(() => {
    const data = historical_data[period];
    const series_data = data.map(x => [x.timestamp, x.value]);
    const isProfit = data[0].value < data[data.length - 1].value;

    let xLabelFormat = '{HH}h';
    switch (period) {
      case 'seven_days':
      case 'thirty_days':
      case 'ninety_days':
        xLabelFormat = '{MMM} {d}';
        break;
      case 'all':
        xLabelFormat = '{MMM} {yyyy}';
        break;
      default:
        break;
    }

    return {
      backgroundColor: 'transparent',
      connectNulls: true,
      grid: { bottom: 25, left: 5, right: 50, top: 20 },
      legend: { show: false },
      series: [
        {
          areaStyle: {
            color: {
              colorStops: [
                {
                  offset: 0,
                  color: isProfit
                    ? 'rgba(22, 199, 132, 0.3)'
                    : 'rgb(234, 57, 67, 0.3)'
                },
                {
                  offset: 1,
                  color: isProfit
                    ? 'rgba(22, 199, 132, 0)'
                    : 'rgb(234, 57, 67, 0)'
                }
              ],
              global: false,
              x: 0,
              x2: 0,
              y: 0,
              y2: 1
            }
          },
          color: isProfit ? '#16C784' : '#EA3943',
          data: series_data,
          itemStyle: { normal: { showSymbol: false } },
          name: 'Total value',
          symbol: 'none',
          symbolSize: 0,
          type: 'line'
        }
      ],
      tooltip: {
        confine: true,
        trigger: 'axis',
        extraCssText: 'padding: 0px; border: none;',
        axisPointer: {
          type: 'cross',
          label: { show: false }
        },
        formatter: function (
          params: [{ data: [Date, number]; color: string; seriesName: string }]
        ) {
          const data = params?.[0]?.data;
          const date = data?.[0] ? new Date(data[0]) : new Date();

          const date_string = date.toDateString();
          const time_string = date.toTimeString().split(' ')[0].substring(0, 5);
          const utc = date.toTimeString().split(' ')[1];
          const value = formatNumber(data[1], { currency: 'USD' });

          return `
            <div style="background-color: #303a4b; border: none; border-radius: 3px; padding: 0.5rem">
              <div style="display: flex; flex-direction: row; align-items: center; justify-content: between; gap: 1rem">
                <div style="display: flex; flex-direction: column; align-items: start; justify-content: center;">
                  <div style="font-size: 12px; color: #fff; font-weight: 400;">${date_string}</div>
                  <div style="font-size: 12px; color: #94a3b8; font-weight: 400;">
                    ${time_string} <small>${utc}</small>
                  </div>
                </div>
                <div style="color: ${params[0].color}; padding-right: 0.5rem">${value}</div>
              </div>
            </div>
            `;
        }
      },
      xAxis: {
        axisLabel: {
          color: '#646B80',
          fontSize: 10,
          margin: 7,
          show: true,
          hideOverlap: true,
          formatter: xLabelFormat
        },
        axisLine: {
          lineStyle: { color: '#616E85' }
        },
        splitNumber: 5,
        type: 'time'
      },
      yAxis: {
        axisLabel: {
          color: '#A1A7BB',
          fontSize: 10,
          show: true
        },
        nameLocation: 'start',
        nameTextStyle: {
          fontSize: 11,
          fontWeight: 600,
          padding: [15, 0, 0, 20]
        },
        position: 'right',
        scale: true,
        show: true,
        splitLine: {
          lineStyle: { color: '#313544' },
          show: true
        },
        splitNumber: 5,
        type: 'value'
      }
    };
  }, [period]);

  const handleHistoryPeriodChange = (selectedPeriod: string) => {
    setPeriod(selectedPeriod);
  };

  return (
    <div className="flex flex-col w-full">
      <div className="flex items-center justify-between">
        <h1 className="text-xl font-extrabold">History</h1>
        <Tabs
          defaultValue="twenty_four_hours"
          className="w-[300px]"
          onValueChange={handleHistoryPeriodChange}
        >
          <TabsList className="grid w-full grid-cols-5 p-0.5 h-auto">
            <TabsTrigger value="twenty_four_hours">24H</TabsTrigger>
            <TabsTrigger value="seven_days">7D</TabsTrigger>
            <TabsTrigger value="thirty_days">30D</TabsTrigger>
            <TabsTrigger value="ninety_days">90D</TabsTrigger>
            <TabsTrigger value="all">ALL</TabsTrigger>
          </TabsList>
        </Tabs>
      </div>
      <ReactECharts
        option={option}
        theme="dark"
        style={{ height: '240px', width: '100%' }}
        showLoading={false}
        notMerge={true}
        lazyUpdate={true}
      />
    </div>
  );
};
